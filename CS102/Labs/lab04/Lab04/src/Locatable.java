/**
 * Locatable interface for Lab04 assignment.
 */
public interface Locatable {

    /**
     * This method gets the X position of the implemented object.
     */
    int getX();

    /**
     * This method gets the Y position of the implemented object.
     */
    int getY();

    /**
     * This method sets the X & Y position of the implemented object.
     */
    void setPos( int posX, int posY );
}
